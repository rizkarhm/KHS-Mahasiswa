    private double skala(String predikat) {
        switch (predikat) {
            case "A":
                Skala = 4;
                break;
            case "B+":
                Skala = 3.5;
                break;
            case "B":
                Skala = 3;
                break;
            case "C+":
                Skala = 2.5;
                break;
            case "C":
                Skala = 2;
                break;
            case "D+":
                Skala = 1.5;
                break;
            case "D":
                Skala = 1;
                break;
            case "E":
                Skala = 0;
                break;
            default:
                System.out.println("");
                break;
        }
        return Skala;
    }

    private String predikat(double NA) {
        if (NA > 80) {
            predikat = "A";
        } else if (75 < NA && NA <= 80) {
            predikat = "B+";
        } else if (70 < NA && NA <= 75) {
            predikat = "B";
        } else if (65 < NA && NA <= 70) {
            predikat = "C+";
        } else if (60 < NA && NA <= 65) {
            predikat = "C";
        } else if (55 < NA && NA <= 60) {
            predikat = "D+";
        } else if (50 < NA && NA <= 55) {
            predikat = "D";
        } else if (NA < 50) {
            predikat = "E";
        } else {
            System.out.println("");
        }
        return predikat;
    }

    private double hitungNA() {
        NA = (UTS * 0.15) + (UAS * 0.15) + (UKK * 0.7);
        return NA;
    }

