    private void BTAMBAHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTAMBAHActionPerformed
        // TODO add your handling code here:
        matkul = CBMATKUL.getSelectedItem().toString();
        sks = TSKS.getText();
        uts = TUTS.getText();
        uas = TUAS.getText();
        ukk = TUKK.getText();

        try {
            if (CBMATKUL.getSelectedIndex()== 0) {
                JOptionPane.showMessageDialog(this, "Pilih Mata Kuliah terlebih dahulu",
                        "Terjadi Kesalahan", JOptionPane.ERROR_MESSAGE);
            } else if (uts.equals("") && uas.equals("") && ukk.equals("")) {
                JOptionPane.showMessageDialog(this, "Masukkan nilai terlebih "
                        + "dahulu", "Terjadi Kesalahan", JOptionPane.ERROR_MESSAGE);
            } else if (uts.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Masukkan nilai UTS",
                        "Terjadi Kesalahan", JOptionPane.ERROR_MESSAGE);
            } else if (uas.equals("")) {
                JOptionPane.showMessageDialog(this, "Masukkan nilai UAS",
                        "Terjadi Kesalahan", JOptionPane.ERROR_MESSAGE);
            } else if (ukk.equals("")) {
                JOptionPane.showMessageDialog(this, "Masukkan nilai UKK",
                        "Terjadi Kesalahan", JOptionPane.ERROR_MESSAGE);
            }  else {
                int jumlah = TABELKHS.getRowCount();
                int i = 1;
                do {
                    UTS = Double.parseDouble(uts);
                    UAS = Double.parseDouble(uas);
                    UKK = Double.parseDouble(ukk);
                    SKS = Double.parseDouble(sks);

                    hitungNA();
                    predikat = predikat(NA);
                    Skala = skala(predikat);
                    total = Skala * SKS;
                    JTOTAL += total;
                    JSKS += SKS;
                    i++;
                } while (i <= jumlah - (jumlah - 1));

                na = Double.toString(NA);
                IPK = JTOTAL / JSKS;
                ipk = Double.toString(IPK);

                Object data[] = {matkul, sks, na, predikat};
                tabel.addRow(data);
                TABELKHS.setModel(tabel);

                CBMATKUL.setSelectedIndex(0);
                TUTS.setText("");
                TUAS.setText("");
                TUKK.setText("");
                TIPK.setText(ipk);
            }
        } catch (NumberFormatException e) {
            //validasi hanya angka 
            JOptionPane.showMessageDialog(this, "Masukan jumlah harus berupa "
                    + "angka", "Terjadi Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_BTAMBAHActionPerformed

    private void BRESETActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BRESETActionPerformed
        // TODO add your handling code here:
        if (TABELKHS.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Maaf, tabel kosong",
                    "KHS Mahasiswa", JOptionPane.ERROR_MESSAGE);
        } else {

            int result = JOptionPane.showConfirmDialog(this,
                    "Anda yakin ingin menghapus semua data pada tabel?",
                    "Reset",
                    JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                clear();
                tabel.getDataVector().removeAllElements();
                tabel.fireTableDataChanged();
            }
        }
    }//GEN-LAST:event_BRESETActionPerformed

